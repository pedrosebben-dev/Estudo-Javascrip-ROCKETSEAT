let number = 10


// INCREMENTO 

// number = number + 1

number ++


// Incrementa após por isso nao mostra no console
console.log(number++)
console.log(number)


// Incrementa antes

console.log(++number)


// Decremento

console.log("Decremento após ", number--)
console.log(number--)

// Decremento Antes
console.log(--number)